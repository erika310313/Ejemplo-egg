
package ClasesEjemploDeLaGuia;


public class Ejemplo03_MetodoMath {

   
    public static void main(String[] args) {
        //Método abs - Retorna el número absoluto del parámetro double
        double valorAbsoluto = Math.abs(-2.3);
        System.out.println("Valor absoluto: " + valorAbsoluto);
      
      
        //Método sin - Retorna el seno del ángulo del parámetro
        double seno = Math.sin(0.5);
        System.out.println("Seno: " + seno);
        
        
        //Método cos - Retorna el coseno del ángulo del parámetro
        double coseno = Math.cos(0.5);
        System.out.println("Coseno: " + coseno);
        
        
        //Método tan - Retorna la tangente del ángulo del parámetro
        double tangente = Math.tan(0.5);
        System.out.println("Tangente: " + tangente);
        
        
        //Método log - Retorna el logaritmo natural del valor del parámetro
        double logaritmo = Math.log(2.7172);
        System.out.println("Logaritmo: " + logaritmo);
        
        
        //Método pow - Retorna la potencia del primer parámetro elevado al segundo parámetro
        double potencia = Math.pow(2.3, 1.0);
        System.out.println("Potencia: " + potencia);
        
        
        //Método round - Retorna el  número long más cercano del parámetro double
        double redondeo = Math.round(2.5);
        System.out.println("Redondeo: " + redondeo);
        
        
        //Método random -   Retorna un número double aleatorio entre 0.0 y 1.0
        double aleatorio = Math.random();
        System.out.println("Aleatorio: " + aleatorio);
          
          
                
      
        
            
    }
  
}
